        public static void shuffleArray(String[] array) {
            Random rand = new Random();
            System.out.println("Adlar qarisdirilir...\n");
            for (int i = array.length - 1; i > 0; i--) {
                int j = rand.nextInt(i + 1);
                // İki elementi dəyişdir
                String temp = array[i];
                array[i] = array[j];
                array[j] = temp;
            }
        }
